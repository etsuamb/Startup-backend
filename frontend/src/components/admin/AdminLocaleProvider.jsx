const AdminLocaleContext = createContext(null);

function format(template, values) {
	return Object.entries(values || {}).reduce(
		(result, [key, value]) => result.replaceAll(`{${key}}`, String(value)),
		template,
	);
}

export function AdminLocaleProvider({ children }) {
	const [locale, setLocaleState] = useState(DEFAULT_LOCALE);
	const originalTextRef = useRef(new WeakMap());

	const setLocale = useCallback((nextLocale) => {
		const normalized = nextLocale === "am" || nextLocale === "Amharic" ? "am" : "en";
		setLocaleState(normalized);
		window.localStorage.setItem(STORAGE_KEY, normalized);
		document.cookie = `admin_locale=${normalized}; path=/; max-age=31536000; samesite=lax`;
		document.documentElement.lang = normalized;
	}, []);

	useEffect(() => {
		queueMicrotask(() => {
			const stored = window.localStorage.getItem(STORAGE_KEY);
			if (stored === "am" || stored === "en") setLocale(stored);
		});
	}, [setLocale]);

	useEffect(() => {
		const root = document.querySelector("[data-admin-locale-root]");
		if (!root) return undefined;

		const translateText = (node) => {
			if (!originalTextRef.current.has(node)) originalTextRef.current.set(node, node.textContent);
			const original = originalTextRef.current.get(node);
			const trimmed = original.trim();
			const translated = locale === "am" ? translateVisibleText(trimmed) : null;
			node.textContent = translated ? original.replace(trimmed, translated) : original;
		};
		const translateAttributes = (element) => {
			for (const attribute of ["placeholder", "title", "aria-label"]) {
				if (!element.hasAttribute(attribute)) continue;
				const originalKey = `data-admin-original-${attribute}`;
				if (!element.hasAttribute(originalKey)) {
					element.setAttribute(originalKey, element.getAttribute(attribute));
				}
				const original = element.getAttribute(originalKey);
				element.setAttribute(attribute, locale === "am" ? translateVisibleText(original) : original);
			}
		};
		const translateTree = (node) => {
			if (node.nodeType === Node.TEXT_NODE) {
				translateText(node);
				return;
			}
			if (node.nodeType !== Node.ELEMENT_NODE) return;
			translateAttributes(node);
			for (const child of node.childNodes) translateTree(child);
		};

		translateTree(root);
		const observer = new MutationObserver((mutations) => {
			for (const mutation of mutations) {
				for (const node of mutation.addedNodes) translateTree(node);
			}
		});
		observer.observe(root, { childList: true, subtree: true });
		return () => observer.disconnect();
	}, [locale]);

	const value = useMemo(() => {
		const t = (key, values) => format(messages[locale]?.[key] || messages.en[key] || key, values);
		return {
			locale,
			language: locale === "am" ? "Amharic" : "English",
			dateLocale: locale === "am" ? "am-ET" : "en-US",
			setLocale,
			t,
		};
	}, [locale, setLocale]);

	return <AdminLocaleContext.Provider value={value}>{children}</AdminLocaleContext.Provider>;
}

export function useAdminLocale() {
	const context = useContext(AdminLocaleContext);
	if (!context) throw new Error("useAdminLocale must be used inside AdminLocaleProvider");
	return context;
}
